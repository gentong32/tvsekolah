          <ol>
              <li style="font-weight: bold">
                  Apakah TV Sekolah sama dengan upload YouTube?<br>
                  <span style="font-weight: normal">
                      Kita memanfaatkan YouTube sebagai Media Aset Management online untuk bahan siaran.
					  Jadi semua konten harus ada di YouTube (diunggah ke YouTube dulu) baru linknya diunggah ke aplikasi.
					  Di aplikasi konten tersebut harus disetujui oleh verifikator sekolah untuk bisa tampil di Perpustakaan Digital.
                  </span>
              </li>
              <li style="font-weight: bold">
                  Apakah kelebihan TV Sekolah dibanding dengan upload YouTube biasa?<br>
                  <span style="font-weight: normal">
                      Kelebihannya adalah konten telah diverifikasi oleh pengelola/verifikator sekolah.
					  Khusus untuk konten pembelajaran guru harus menyesuaikan KI/KD yang sudah ada di database.
                  </span>
              </li>
              <li style="font-weight: bold">
                  Dalam beberapa contoh jadwal program ada jam-jamnya. Apakah pembelajaran sekolah yang tidak pakai jam bisa diputar orang? <br>
                  <span style="font-weight: normal">
                      Ada 4 fitur utama di TV Sekolah:
                      <ol>
                          <li>Panggung Sekolah (TV Streaming) yang mengalir berdasarkan jadwal siaran.</li>
                          <li>Perpustakaan Digital (video on demand) yang bisa diputar kapan saja sesuai keinginan.</li>
                      <li>Kelas Virtual dimana guru dapat membuat kelas virtual dengan menyusun beberapa video pembelajaran dalam sebuah learning path(playlist mini) yang dilengkapi dengan soal latihan dan fitur lainnya.</li>
						  <li>Festival TV Sekolah yaitu kegiatan di seluruh fitur TV Sekolah dilombakan dalam event tahunan mulai tingkat kabupaten kota, propinsi, dan nasional <i>(underconstruction)</i>.</li>
                  </span>
              </li>
          </ol>
